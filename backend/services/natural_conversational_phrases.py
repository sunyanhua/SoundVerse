def get_natural_phrases() -> list[str]:
    """返回所有自然短语"""
    return NATURAL_CONVERSATIONAL_PHRASES + REAL_AUDIO_PHRASES

def get_random_phrases(count: int = 20) -> list[str]:
    """随机返回指定数量的自然短语"""
    import random
    all_phrases = get_natural_phrases()
    if count >= len(all_phrases):
        return all_phrases.copy()
    return random.sample(all_phrases, count)